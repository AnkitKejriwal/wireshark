static rd_vsa_table *get_vsa_table(guint32 type)
{
    guint32 i = 0;

    while (radius_vsa_table[i].type) {
	if (radius_vsa_table[i].type == type)
	    return(&radius_vsa_table[i]);
	i++;
    }
    return(0);
}

static guint32 match_numval(guint32 val, const value_value_pair *vs)
{
  guint32 i = 0;

  while (vs[i].val1) {
    if (vs[i].val1 == val)
      return(vs[i].val2);
    i++;
  }

  return(0);
}

